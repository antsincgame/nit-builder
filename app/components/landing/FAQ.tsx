export default function FAQ() {
  const [open, setOpen] = useState<number | null>(0);
  const ref = useRef<HTMLElement>(null);

  useEffect(() => {
    const el = ref.current;
    if (!el) return;
    const observer = new IntersectionObserver(
      (entries) => entries.forEach((e) => { if (e.isIntersecting) e.target.classList.add("visible"); }),
      { threshold: 0.03 }
    );
    el.querySelectorAll(".fade-in-up").forEach((e) => observer.observe(e));
    return () => observer.disconnect();
  }, []);

  return (
    <section id="faq" ref={ref} className="py-24">
      <div className="max-w-3xl mx-auto px-5 sm:px-8">
        <div className="text-center mb-14 fade-in-up">
          <h2 className="text-3xl sm:text-4xl font-bold tracking-tight text-white mb-4">
            Вопросы о бесплатном создании сайтов через ИИ
          </h2>
          <p className="text-[#71717A] text-lg">
            Ответы на главные вопросы про генератор сайтов на нейросети nitgen
          </p>
        </div>

        <div className="flex flex-col gap-2">
          {faqs.map((faq, i) => (
            <div
              key={i}
              className="fade-in-up rounded-xl border border-white/[0.06] bg-[#141414] overflow-hidden hover:border-white/[0.10] transition-colors"
              style={{ transitionDelay: `${(i % 5) * 40}ms` }}
            >
              <button
                className="w-full px-6 py-4 flex items-center justify-between gap-4 text-left"
                onClick={() => setOpen(open === i ? null : i)}
              >
                <span className="text-sm font-medium text-white pr-4">{faq.q}</span>
                <ChevronDown
                  size={15}
                  className={`text-[#71717A] flex-shrink-0 transition-transform duration-200 ${open === i ? "rotate-180" : ""}`}
                />
              </button>
              <div
                className="overflow-hidden transition-all duration-300"
                style={{ maxHeight: open === i ? "500px" : "0px" }}
              >
                <p className="px-6 pb-5 text-sm text-[#71717A] leading-relaxed">{faq.a}</p>
              </div>
            </div>
          ))}
        </div>

        <div className="mt-12 fade-in-up">
          <p className="text-center text-sm text-[#71717A] leading-relaxed">
            Остались вопросы о том, как бесплатно создать сайт с помощью ИИ?
            Напишите нам — поможем разобраться с генерацией сайтов через нейросеть.
          </p>
        </div>
      </div>
    </section>
  );
}
