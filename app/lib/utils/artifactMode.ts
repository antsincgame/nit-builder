export type ArtifactMode = "template" | "custom" | "auto" | "php-sqlite";

/**
 * Определяет, нужен ли режим работающего бэкенда (PHP + SQLite: БД, серверная
 * логика, админка) вместо статического HTML-лендинга.
 *
 * Раньше функция требовала ЯВНО упомянуть стек («PHP»/«SQLite»/«MySQL») —
 * иначе любой коммерческий запрос («магазин с корзиной и админкой») молча
 * собирался как статический лендинг. Это утечка технической детали в UX:
 * непрограммист не знает слова «PHP» и не понимает, почему его «админка» не
 * заработала. Теперь решаем по СМЫСЛУ намерения:
 *
 *  - Сильные функциональные сигналы (корзина, оформление/приём заказов,
 *    админка, управление товарами/каталогом, личный кабинет, хранение заявок)
 *    — это фичи, которых без сервера и БД попросту не существует. Любого из
 *    них достаточно, чтобы включить php-sqlite.
 *  - Явное упоминание стека (php/sqlite/mysql/pdo) тоже включает — пользователь
 *    прямо просит бэкенд.
 *  - Чисто промо-формулировки («лендинг кофейни», «магазин одежды — витрина»,
 *    «портфолио») остаются статическим лендингом: голые «магазин»/«товары» НЕ
 *    триггерят, потому что это сплошь и рядом обычные промо-страницы.
 */
export function inferArtifactModeFromPrompt(
  prompt: string,
): Extract<ArtifactMode, "php-sqlite"> | undefined {
  const text = prompt.toLowerCase();

  // Явный технический стек — пользователь прямо просит бэкенд.
  const explicitStack =
    /\b(php|sqlite|mysql|pdo)\b/i.test(text) || /(пхп|майскл|мускул)/i.test(text);

  // Сильные функциональные сигналы намерения: каждая фича подразумевает
  // работающую серверную логику и хранилище (БД), которых в статическом HTML
  // нет. Любого совпадения достаточно для php-sqlite. Список — подстроки в
  // нижнем регистре (учитывает словоформы за счёт усечённых корней).
  const STRONG_SIGNALS = [
    // commerce-логика
    "корзин",
    "оформление заказа",
    "оформить заказ",
    "checkout",
    "чекаут",
    "добавить в корзину",
    "интернет-магазин",
    "интернет магазин",
    "онлайн-магазин",
    "онлайн магазин",
    "e-commerce",
    "ecommerce",
    "принимать заказы",
    "приём заказов",
    "прием заказов",
    "принимать заявки",
    // админка / управление контентом
    "админк",
    "админ-панель",
    "админ панель",
    "admin panel",
    "панель управления",
    "управление товар",
    "управлять товар",
    "управление каталог",
    "управление заказ",
    "добавлять товар",
    "редактировать товар",
    "crud",
    // аккаунты / хранение данных
    "личный кабинет",
    "регистрация и вход",
    "база заявок",
    "база заказов",
    "сохранять заявки",
    "сохранять заказы",
  ];
  const hasStrongSignal = STRONG_SIGNALS.some((kw) => text.includes(kw));

  return explicitStack || hasStrongSignal ? "php-sqlite" : undefined;
}
